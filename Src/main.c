/**
  * @brief  Main program
  * @param  None
  * @retval None
  */
int main(void)
{

  /* STM32F4xx HAL library initialization:
       - Configure the Flash prefetch, Flash preread and Buffer caches
       - Systick timer is configured by default as source of time base, but user 
             can eventually implement his proper time base source (a general purpose 
             timer for example or other time source), keeping in mind that Time base 
             duration should be kept 1ms since PPP_TIMEOUT_VALUEs are defined and 
             handled in milliseconds basis.
       - Low Level Initialization
     */
  HAL_Init();

  /* Configure the system clock to 168 MHz */
  SystemClock_Config();

  /* Configure the BSP */
  BSP_Config();

  /* Initialize the LwIP stack */
  lwip_init();

  /* Configure the Network interface */
  Netif_Config();

  /* Initialize the FTP server */
  ftpd_init();

  /* Notify user about the network interface config */
  User_notification(&gnetif);

  /* Init Host Library */
  USBH_Init(&hUSBHost, USBH_UserProcess, 0);

  /* Add Supported Class */
  USBH_RegisterClass(&hUSBHost, USBH_MSC_CLASS);

  /* Start Host Process */
  USBH_Start(&hUSBHost);

  FATFS_LinkDriver(&USBH_Driver, Path);

  MonitorDriveMedia();

  /* Infinite loop */
  while (1)
  {
	  /* USB Host Background task */
	  USBH_Process(&hUSBHost);

	  MonitorDriveMedia();

	  if(mediaPresent == 0)
		  f_mount(NULL, (TCHAR const*)Path, 1);

	  /* Read a received packet from the Ethernet buffers and send it
	   	   to the lwIP for handling */
	  ethernetif_input(&gnetif);

	  /* Handle timeouts */
	  sys_check_timeouts();

#ifdef USE_DHCP
	  /* handle periodic timers for LwIP */
	  DHCP_Periodic_Handle(&gnetif);
#endif
  }
}

/**
  * @brief  Setup the BSP.
  * @param  None
  * @retval None
  */
static void BSP_Config(void)
{
	/* Configure LED1, LED2, LED3 and LED4 */
	BSP_LED_Init(LED3);
	BSP_LED_Init(LED4);

	/* Set Systick Interrupt to the highest priority */
	HAL_NVIC_SetPriority(SysTick_IRQn, 0x0, 0x0);
}

/**
  * @brief  Setup the network interface
  * @param  None
  * @retval None
  */
static void Netif_Config(void)
{
  ip_addr_t ipaddr;
  ip_addr_t netmask;
  ip_addr_t gw;

#ifdef USE_DHCP
  ip_addr_set_zero_ip4(&ipaddr);
  ip_addr_set_zero_ip4(&netmask);
  ip_addr_set_zero_ip4(&gw);
#else
  IP_ADDR4(&ipaddr,IP_ADDR0,IP_ADDR1,IP_ADDR2,IP_ADDR3);
  IP_ADDR4(&netmask,NETMASK_ADDR0,NETMASK_ADDR1,NETMASK_ADDR2,NETMASK_ADDR3);
  IP_ADDR4(&gw,GW_ADDR0,GW_ADDR1,GW_ADDR2,GW_ADDR3);
#endif /* USE_DHCP */

  /* add the network interface */
  netif_add(&gnetif, &ipaddr, &netmask, &gw, NULL, &ethernetif_init, &ethernet_input);

  /*  Registers the default network interface */
  netif_set_default(&gnetif);

  if (netif_is_link_up(&gnetif))
  {
    /* When the netif is fully configured this function must be called */
    netif_set_up(&gnetif);
  }
  else
  {
    /* When the netif link is down this function must be called */
    netif_set_down(&gnetif);
  }

  /* Set the link callback function, this function is called on change of link status*/
  netif_set_link_callback(&gnetif, ethernetif_update_config);
}

/**
  * @brief
  * @param
  * @retval
  */
void    MonitorDriveMedia(void) {
	BYTE        mediaPresentNow;

	mediaPresentNow = !disk_status (0);

	if(mediaPresentNow != mediaPresent)
	{
		if(mediaPresentNow)
		{
			if( disk.drv[0]->disk_initialize(disk.lun[0]) != STA_NOINIT )
			{
				if( f_mount(&FatFs, (TCHAR const*)Path, 1) == FR_OK )
				{
					mediaPresent = 1;
				}
				else
				{
					mediaPresent = 0;
				}
			}
			else
			{
				mediaPresent = 0;
			}
		}
		else
		{
			mediaPresent = 0;
		}
	}
}

